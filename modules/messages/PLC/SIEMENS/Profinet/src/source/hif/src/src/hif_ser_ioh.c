            if(HIF_IS_NOT_NULL(new_rqb->args.job_info_ind) && HIF_IS_NOT_NULL(*((LSA_UINT32*)help_ptr))) /*#19*/
            {
                /* INFO: no need to deserialize hif-upper pointer: IOH_JOB_INFO_IND_PTR_TYPE */
                help_ptr+=HIF_SER_SIZEOF(4);
                HIF_SER_COPY_SHMTH_16((LSA_UINT16*)&new_rqb->args.job_info_ind->old_state,(LSA_UINT32**)&help_ptr);                /* old_state */
                HIF_SER_COPY_SHMTH_16((LSA_UINT16*)&new_rqb->args.job_info_ind->new_state,(LSA_UINT32**)&help_ptr);                /* new_state */
                HIF_SER_COPY_SHMTH_16((LSA_UINT16*)&new_rqb->args.job_info_ind->reason,(LSA_UINT32**)&help_ptr);                /* reason */
            }
